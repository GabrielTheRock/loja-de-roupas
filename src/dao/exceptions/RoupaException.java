package dao.exceptions;

public class RoupaException extends Exception {

    public RoupaException() {
    }

    public RoupaException(String arg0) {
        super(arg0);
    }

    public RoupaException(Throwable arg0) {
        super(arg0);
    }

    public RoupaException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public RoupaException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
        super(arg0, arg1, arg2, arg3);
    }
}
