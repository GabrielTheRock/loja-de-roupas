package dao.exceptions;

public class RoupasException extends Exception {

    public RoupasException() {
    }

    public RoupasException(String arg0) {
        super(arg0);
    }

    public RoupasException(Throwable arg0) {
        super(arg0);
    }

    public RoupasException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public RoupasException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
        super(arg0, arg1, arg2, arg3);
    }
}
